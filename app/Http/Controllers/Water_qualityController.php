     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $client = new Client();
        $response = $client->request('GET', 'https://environment.data.gov.uk/catchment-planning/def/waterbody-classification/classification-value.json');
        $data = json_decode($response->getBody()->getContents(), true);
        foreach ($data['items'] as $item){
            $water_quality = new Water_quality;
            $water_quality->name = $item['label'];
            $water_quality->save();
        }
    }

